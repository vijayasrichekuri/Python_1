
# Topic: for Loop in Python
# A 'for' loop is used to iterate over a sequence (list, range, etc.)
# It repeats the block of code for each item in the sequence.
print("Printing numbers from 1 to 5:")
for i in range(1, 6):  # range(1,6) means numbers 1 to 5
    print("Number:", i)
# Explanation:
# range(1,6) → generates numbers 1,2,3,4,5
# 'i' → loop variable that takes each value one by one
# print() → displays each value of i
