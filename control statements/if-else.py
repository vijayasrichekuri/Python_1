
# Topic: if-else Statement in Python
# The 'if-else' statement allows decision-making in programs.
# Based on a condition, the code executes one block or another.
# Input from the user
num = int(input("Enter a number: "))
# Checking if the number is positive, zero, or negative
if num > 0:
    print("The number is Positive.")
elif num == 0:
    print("The number is Zero.")
else:
    print("The number is Negative.")
# Explanation:
# if → checks the first condition
# elif → checks next condition only if the first is false
# else → executes if all above conditions are false

