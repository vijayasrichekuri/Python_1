# Topic: while Loop in Python
# The 'while' loop executes a block as long as the condition is True.

count = 1
print("Counting up to 5 using while loop:")

while count <= 5:  # loop continues until count becomes 6
    print("Count:", count)
    count += 1  # increment count by 1

# while condition → keeps running while True
# count += 1 → updates variable to avoid infinite loop

