# A List is an ordered, mutable (changeable) collection of items.
# Lists can store elements of different data types.

fruits = ["apple", "banana", "cherry"]  # create a list
fruits.append("mango")  # add new item at the end
print("Fruits list:", fruits)
print("First fruit:", fruits[0])  # access first element
print("Total fruits:", len(fruits))  # length of list
