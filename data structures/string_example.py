
# Topic: String in Python
# A String is a sequence of characters enclosed in quotes.
# Strings are immutable and support many operations.

name = "Python Programming"

print("Original String:", name)
print("Lowercase:", name.lower())  # converts to lowercase
print("Uppercase:", name.upper())  # converts to uppercase
print("Reversed:", name[::-1])     # reverses the string

